    <h1 class="text-center"id="about">About SHOP</h1>
<P >
Wall to wall chicken recipes! A great book from the 101 Good Food series of little cook books, packed full of wonderful chicken recipes. Most of these recipes are very good giving you lots of ideas.
We eat quite Chicken at least twice a week in my household as everyone in my family loves chicken, it is a versatile meat and can be used in a variety of ways and this book gives some great examples of that. Some of the recipes are not to my taste but I have modified one or two to suit the family and they have worked out perfectly ok.
If nothing else it gives you some good ideas for dinner for instance, salads, warm and cold with chicken. Warm Chicken with Avocado for one, using romaine lettuce and sunflower seeds, Chicken and pepper Salad Bowl, with some lovely crisp continental lettuce. A nice amount of Noodle recipes, one being Chicken Pot Noodle that appealed to one of my brood! Some gorgeous baked Chicken dishes, Mustard Stuffed Chicken and Lemon Tarragon Chicken in a rich creamy sauce. Casseroles, rice dishes, and loads of ways to Roast Chicken to perk up the Sunday Roast, making it interesting (I too love just Roast Chicken but its nice to vary it from time to time).
Not all will be to every ones taste and there are a few I wouldn't touch with a barge pole but never the less a great book. Use mine very regularly. Would recommend to all lovers of Chicken out there.
</P>



